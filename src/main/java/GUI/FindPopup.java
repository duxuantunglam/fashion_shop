    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        // TODO add your handling code here:
        input = txtInput.getText();
        txtFindby.setText(findby);
        if(null != txtFindby.getText())
            switch (txtFindby.getText()){
            case "Name":
                FindByName.findByName(input, fashionList, listFind);
                trangchu.showTable(trangchu.getListFind());
                break;
            case "Id":
                FindById.findById(input, fashionList, listFind);
                trangchu.showTable(trangchu.getListFind());
                break;
            case "Color":
                FindByColor.findByColor(input, fashionList, listFind);
                trangchu.showTable(trangchu.getListFind());
                break;
            case "Material":
                FindByMaterial.findByMaterial(input, fashionList, listFind);
                trangchu.showTable(trangchu.getListFind());
                break;
            case "Brand":
                FindByBrand.findByBrand(input, fashionList, listFind);
                trangchu.showTable(trangchu.getListFind());
                break;
            case "Length":
                FindByLength.findByLength(input, fashionList, listFind);
                trangchu.showTable(trangchu.getListFind());
                break;
            case "Width":
                FindByWidth.findByWidth(input, fashionList, listFind);
                trangchu.showTable(trangchu.getListFind());
                break;
            case "Month":
                FindByMonth.findByMonth(input, fashionList, listFind);
                trangchu.showTable(trangchu.getListFind());
                break;
            default:
                break;
        }
        
        dispose();
    }//GEN-LAST:event_jLabel3MouseClicked

